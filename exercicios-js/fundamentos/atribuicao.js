const a = 7
let b = 3

b += a // Atribuição aditiva: b recebe b + a
console.log(b)

b -= 4 // Atribuição subtrativa: b recebe b - 4
console.log(b)

b *= 2 // Atribuição multiplicativa: b recebe b * 2
console.log(b)

b /= 2 // Atribuição divisiva: b recebe b / 2
console.log(b)

b %= 2 // Atribuição modular: b recebe b % 2 --- resto da 'primeira' divisão de b por 2
console.log(b)
