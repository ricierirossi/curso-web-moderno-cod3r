// Código NÃO Executável

// Procedural
processamento(valor1, valor2, valor3)

// Orientação Objeto

objeto = {
    valor1, 
    valor2,
    valor3,
    processamento() {
        //...
    }
}

objeto.processamento() // O foco passou a ser objeto, e antes era função

/* 
    Princípios importantes:
    1. abstração (trazer as características pertinentes do objeto para o sistema)
    2. encapsulamento (capacidade de uma função ou objeto de limitar o acesso a certas variáveis ou métodos, ocultando sua complexidade 
                       interna e protegendo-os de serem alterados por outros códigos externos.) 
    3. herença (receber atributos, características e comportamentos de um objeto pai)
    4. polimorfismo (se tem uma variável pai de um tipo mais genérico que pode ser atribuiada com um objeto menos genérico
                     exemplo: pai: Carro, filho: Ferrari)
*/